export const maskRGorCNH = (value: string) => {
  const numbers = value.replace(/\D/g, "");

  // CNH has 11 digits
  if (numbers.length === 11) {
    return numbers.replace(/(\d{3})(\d{3})(\d{4})(\d{1})/, "$1.$2.$3-$4");
  }

  // RG mask (default)
  return value
    .replace(/\D/g, "")
    .replace(/(\d{2})(\d)/, "$1.$2")
    .replace(/(\d{3})(\d)/, "$1.$2")
    .replace(/(\d{3})(\d{1,2})/, "$1-$2")
    .replace(/(-\d{1})\d+?$/, "$1");
};

